    private void tbl_takvimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_takvimMouseClicked
        // TODO add your handling code here:
        int row = tbl_takvim.getSelectedRow();
        int column = tbl_takvim.getSelectedColumn();
        if (!dtm.getValueAt(row, column).equals(" ")) {
            String odemeTuru;
            if (((String) cmb_odemeTipi.getSelectedItem()).equals("Peşin")) {
                odemeTuru = "pesin_fiyat";
            } else {
                odemeTuru = "taksitli_fiyat";
            }

            tempKurs = ((acilanKurslar) dtm.getValueAt(row, column));
            StringBuilder sql = new StringBuilder();
            sql.append("SELECT   \"public\".\"fiyatlistesi\".\"" + odemeTuru + "\"\n");
            sql.append("FROM     \"public\".\"acilanKurslar\"\n");
            sql.append("INNER JOIN \"public\".\"kurs\"  ON \"public\".\"acilanKurslar\".\"kurs_id\" = \"public\".\"kurs\".\"id\"\n");
            sql.append("INNER JOIN \"public\".\"fiyatlistesi\"  ON \"public\".\"fiyatlistesi\".\"id\" = \"public\".\"kurs\".\"id\"\n");
            sql.append("WHERE \"acilanKurslar\".\"id\" = " + tempKurs.id);
            int i = 0;
            try (Connection con = DriverManager.getConnection(db_adres, db_users, db_pass);
                    Statement s = con.createStatement();
                    ResultSet rs = s.executeQuery(sql.toString())) {
                while (rs.next()) {
                    i = rs.getInt(odemeTuru);
                }
                StringBuilder t = new StringBuilder();
                t.append(i);
                t.append(" Türk Lirası...");
                lbl_tutar.setText(t.toString());

            } catch (Exception e) {
            }
            if (odemeTuru.equals("pesin_fiyat")) {
                txt_taksit5.setText(String.valueOf(i));

            } else {
                int tempi = i / 5;
                txt_taksit1.setText(String.valueOf(tempi));
                txt_taksit2.setText(String.valueOf(tempi));
                txt_taksit3.setText(String.valueOf(tempi));
                txt_taksit4.setText(String.valueOf(tempi));
                txt_taksit5.setText(String.valueOf(tempi));
            }
        }
    }//GEN-LAST:event_tbl_takvimMouseClicked


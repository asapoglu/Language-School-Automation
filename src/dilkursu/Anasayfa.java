        StringBuilder sql = new StringBuilder();
        sql.append("SELECT   \"public\".\"ogrenci\".\"adi\",\n");
        sql.append("\"public\".\"ogrenci\".\"soyadi\",\n");
        sql.append("\"public\".\"muhasebe\".\"taksit_1\",\n");
        sql.append("\"public\".\"muhasebe\".\"taksit_2\",\n");
        sql.append("\"public\".\"muhasebe\".\"taksit_3\",\n");

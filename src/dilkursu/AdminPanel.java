    private void list_subeOgretmenleriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_subeOgretmenleriMouseClicked
        // TODO add your handling code here:
        dlm_subeOgretmenBeceri.clear();
        tempOgretmen = list_subeOgretmenleri.getSelectedValue();
        StringBuilder sql = new StringBuilder();
        sql.append("SELECT   public.kurs.id, public.kurs.kurs_adi, public.kurs.kur_seviyesi\n");
        sql.append("FROM     public.ogretmenbeceri\n");
        sql.append("INNER JOIN public.ogretmen  ON public.ogretmenbeceri.ogretmen_id = public.ogretmen.id\n");
        sql.append("INNER JOIN public.kurs  ON public.ogretmenbeceri.kurs_id = public.kurs.id\n");
        sql.append("WHERE public.ogretmen.id = ");
        sql.append(tempOgretmen.id);
        try (Connection con = DriverManager.getConnection(db_adres, db_users, db_pass);
                Statement s = con.createStatement();
                ResultSet rs = s.executeQuery(sql.toString());) {
            while (rs.next()) {
                dlm_subeOgretmenBeceri.addElement(new Kurs(rs.getInt("id"), rs.getString("kurs_adi"), rs.getString("kur_seviyesi")));
            }
            list_verebilecekDers.setModel(dlm_subeOgretmenBeceri);
        } catch (SQLException e) {
            jLabel9.setText("Kayıt Başarısız...");
        }
    }//GEN-LAST:event_list_subeOgretmenleriMouseClicked

